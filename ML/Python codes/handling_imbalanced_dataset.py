import numpy as np
import pandas as pd

credit_card = pd.read_csv('/content/credit_data.csv')

credit_card.head()

"""Class --> 1 = fraudulent Transaction   
Class --> 0 = legit transaction
"""

credit_card['Class'].value_counts()

credit_card.shape

"""This is highly imbalanced dataset"""

#seperating legit and fraud transactions
legit = credit_card[credit_card.Class == 0]
fraud = credit_card[credit_card.Class == 1]

legit.shape

fraud.shape

"""
**Under-Sampling**  
buiding a dataset with a similar distribution of legit and fraud transactions
"""

legit_sample = legit.sample(n=25)

legit_sample.shape

"""concatenate legit and fraud"""

new_data=  pd.concat([legit_sample,fraud],axis=0)
#axis =0 means that we want to concatenate rowwise not columnwise

new_data.head()

new_data.tail()

new_data['Class'].value_counts()

